
public class ContinueExample {

	public static void main(String[] args) {
//*****************************************
		for (int i = 0; i < 10; i++) {
			if ( i % 2 == 0)
				continue; // 2로 나눈값이 0이 아닌 값을 출력함
		
			System.out.println("i => " + i);
		}
		
		
		
//******************************************
	}

}
